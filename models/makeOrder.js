import mongoose from 'mongoose';

const orderSchema = mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0
    },
    createdAt: {
        type: Date,
        default: new Date()
    },
})

const MakeOrder = mongoose.model('PostMessage', orderSchema);

export default MakeOrder;